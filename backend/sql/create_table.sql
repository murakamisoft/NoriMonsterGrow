-- プレイヤーマスタの作成
CREATE TABLE m_player( 
    player_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,         -- プレイヤーID
    name VARCHAR2(100) NOT NULL,                                         -- 名前
    lv NUMBER DEFAULT 1,                                                 -- レベル
    experience NUMBER DEFAULT 0,                                         -- 経験値
    created_by VARCHAR2(100),                                            -- 作成者
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,                      -- 作成日時
    updated_by VARCHAR2(100),                                            -- 更新者
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP                       -- 更新日時
); 

COMMENT ON TABLE m_player IS 'プレイヤーの基本情報を管理。名前、レベル、経験値、所持モンスターなど。'; 
COMMENT ON COLUMN m_player.player_id IS 'プレイヤーID'; 
COMMENT ON COLUMN m_player.name IS '名前'; 
COMMENT ON COLUMN m_player.lv IS 'レベル'; 
COMMENT ON COLUMN m_player.experience IS '経験値'; 
COMMENT ON COLUMN m_player.created_at IS '作成日時'; 
COMMENT ON COLUMN m_player.updated_at IS '更新日時'; 
COMMENT ON COLUMN m_player.created_by IS '作成者'; 
COMMENT ON COLUMN m_player.updated_by IS '更新者'; 

-- モンスターマスタの作成
CREATE TABLE m_monster (
    monster_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,      -- モンスターID
    player_id NUMBER NOT NULL,                                          -- プレイヤーID
    monster_type_id NUMBER NOT NULL,                                   -- モンスタータイプID
    monster_attribute_id NUMBER NOT NULL,                              -- モンスター属性ID
    lv NUMBER DEFAULT 1,                                              -- レベルをlvに変更
    created_by VARCHAR2(100),                                          -- 作成者
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,                    -- 作成日時
    updated_by VARCHAR2(100),                                          -- 更新者
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP                     -- 更新日時
); 

COMMENT ON TABLE m_monster IS 'モンスターの基本情報を管理。プレイヤーID、タイプ、属性、レベルなど。'; 
