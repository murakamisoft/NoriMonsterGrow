COMMENT ON COLUMN m_monster.monster_type_id IS 'モンスタータイプID'; 
COMMENT ON COLUMN m_monster.monster_attribute_id IS 'モンスター属性ID'; 
COMMENT ON COLUMN m_monster.lv IS 'レベル'; 
COMMENT ON COLUMN m_monster.created_at IS '作成日時'; 
COMMENT ON COLUMN m_monster.updated_at IS '更新日時'; 
COMMENT ON COLUMN m_monster.created_by IS '作成者'; 
COMMENT ON COLUMN m_monster.updated_by IS '更新者'; 

